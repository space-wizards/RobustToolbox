using System;
using Robust.Client.AutoGenerated;
using Robust.Client.Editor.Interface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Robust.Client.UserInterface.CustomControls;

[GenerateTypedNameReferences]
internal sealed partial class DebugConsoleTab : EditorPanel, IConsoleTab
{
    public event Action<IConsoleTab>? Focused;

    public LineEdit CommandBar => MainControl.CommandBar;

    public DebugConsoleTab()
    {
        RobustXamlLoader.Load(this);
        Title = "Console";

        CommandBar.OnFocusEnter += CommandBarOnOnFocusEnter;
    }

    private void CommandBarOnOnFocusEnter(LineEdit.LineEditEventArgs obj)
    {
        Focused?.Invoke(this);
    }

    protected internal override void TabFocused()
    {
        CommandBar.GrabKeyboardFocus();
    }
}
