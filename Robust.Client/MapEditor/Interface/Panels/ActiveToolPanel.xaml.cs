using Robust.Client.AutoGenerated;
using Robust.Client.Editor.Interface;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.GameObjects;
using Robust.Shared.IoC;

namespace Robust.Client.MapEditor.Interface.Panels;

[GenerateTypedNameReferences]
internal sealed partial class ActiveToolPanel : EditorPanel
{
    [Dependency] private readonly IResourceCache _resourceCache = null!;
    [Dependency] private readonly IEntityManager _entityManager = null!;

    private readonly ClientMapEditorSystem _mapEditor;
    private readonly EntityUid _mapData;

    public ActiveToolPanel(ClientMapEditorSystem mapEditor, EntityUid mapData)
    {
        IoCManager.InjectDependencies(this);

        _mapEditor = mapEditor;
        _mapData = mapData;

        RobustXamlLoader.Load(this);
    }

    protected override void EnteredTree()
    {
        base.EnteredTree();

        _mapEditor.ActiveToolChanged += MapEditorOnActiveToolChanged;
        UpdateActive(_mapEditor.GetActiveTool(_mapData));
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();

        _mapEditor.ActiveToolChanged -= MapEditorOnActiveToolChanged;
    }

    private void MapEditorOnActiveToolChanged(EntityUid map, Entity<MapEditorToolDataComponent>? activeTool)
    {
        if (map != _mapData)
            return;

        UpdateActive(activeTool);
    }

    private void UpdateActive(Entity<MapEditorToolDataComponent>? active)
    {
        ActiveToolContainer.Visible = active.HasValue;
        NoToolContainer.Visible = !active.HasValue;
        if (active != null)
        {
            var control = GetPreviewControl(active.Value, _entityManager, _resourceCache);
            ActiveToolPreview.AddChild(control);
            ActiveToolName.SetMessage(active.Value.Comp.ToolName);
        }
        else
        {
            ActiveToolPreview.RemoveAllChildren();
        }
    }

    internal static Control GetPreviewControl(
        EntityUid entity,
        IEntityManager entityManager,
        IResourceCache resCache)
    {
        var previewEvent = new MapEditorToolMakePreviewControl();
        entityManager.EventBus.RaiseLocalEvent(entity, ref previewEvent);

        var control = previewEvent.Control;
        if (control == null)
        {
            control = new TextureRect
            {
                CanShrink = true,
                Texture = resCache.GetFallback<TextureResource>()
            };
        }

        return control;
    }
}
