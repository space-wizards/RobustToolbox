using Robust.Client.AutoGenerated;
using Robust.Client.Editor.Styling;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.Stylesheets;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.IoC;

namespace Robust.Client.MapEditor.Interface;

[GenerateTypedNameReferences]
internal sealed partial class MapEditorMain : Control
{
    [Dependency] private readonly IEngineStylesheetManager _stylesheetManager = null!;

    public MapEditorMain()
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        MenuBar.Menus.Add(new MenuBar.Menu
        {
            Title = "File",
            Entries =
            {
                new MenuBar.MenuButton
                {
                    Text = "New",
                },
                new MenuBar.MenuSeparator(),
                new MenuBar.MenuButton
                {
                    Text = "Open...",
                },
                new MenuBar.MenuSeparator(),
                new MenuBar.MenuButton
                {
                    Text = "Save",
                },
                new MenuBar.MenuButton
                {
                    Text = "Save All",
                },
                new MenuBar.MenuButton
                {
                    Text = "Save As",
                }
            }
        });

        MenuBar.Menus.Add(new MenuBar.Menu
        {
            Title = "Edit",
            Entries =
            {
                new MenuBar.MenuButton
                {
                    Text = "Undo",
                },
                new MenuBar.MenuButton
                {
                    Text = "Redo",
                }
            }
        });

        MenuBar.Menus.Add(new MenuBar.Menu
        {
            Title = "View"
        });

        MenuBar.Menus.Add(new MenuBar.Menu
        {
            Title = "Window"
        });

        MenuBar.Menus.Add(new MenuBar.Menu
        {
            Title = "Help"
        });
    }

    protected override void EnteredTree()
    {
        base.EnteredTree();

        _stylesheetManager.UseStylesheet(this, sa => sa.GetEditorDark());
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();

        _stylesheetManager.StopStylesheet(this);
    }
}
