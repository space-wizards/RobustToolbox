using System;
using System.Collections.ObjectModel;
using System.Collections.Specialized;
using Robust.Client.AutoGenerated;
using Robust.Client.Editor.Interface;
using Robust.Client.Editor.Styling;
using Robust.Client.MapEditor.Interface.Styling;
using Robust.Client.ResourceManagement;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.GameObjects;
using Robust.Shared.IoC;
using Robust.Shared.Utility;

namespace Robust.Client.MapEditor.Interface.Panels;

[GenerateTypedNameReferences]
internal sealed partial class ToolHistoryPanel : EditorPanel
{
    [Dependency] private readonly IEntityManager _entityManager = null!;
    [Dependency] private readonly IResourceCache _resourceCache = null!;

    private readonly ObservableCollection<EntityUid> _toolHistoryMirror = [];
    private readonly ClientMapEditorSystem _mapEditor;
    private readonly EntityUid _mapData;

    public ToolHistoryPanel(ClientMapEditorSystem mapEditor, EntityUid mapData)
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        _mapEditor = mapEditor;
        _mapData = mapData;

        RecentTools.ItemTemplate = ControlTemplateDelegate.Typed<EntityUid>(CreateHistoryEntry);
        RecentTools.ItemsSource = _toolHistoryMirror;

        Title = "Tool history";
    }

    protected override void EnteredTree()
    {
        base.EnteredTree();

        _mapEditor.ToolHistoryChanged += MapEditorOnToolHistoryChanged;
        MapEditorOnToolHistoryChanged(_mapData,
            new NotifyCollectionChangedEventArgs(NotifyCollectionChangedAction.Reset));
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();

        _mapEditor.ToolHistoryChanged -= MapEditorOnToolHistoryChanged;
    }

    private void MapEditorOnToolHistoryChanged(EntityUid mapData, NotifyCollectionChangedEventArgs changed)
    {
        if (mapData != _mapData)
            return;

        switch (changed.Action)
        {
            case NotifyCollectionChangedAction.Add:
                for (var i = changed.NewStartingIndex; i < changed.NewStartingIndex + changed.NewItems!.Count; i++)
                {
                    _toolHistoryMirror.Insert(i, (EntityUid)changed.NewItems[i]!);
                }

                break;
            case NotifyCollectionChangedAction.Remove:
                for (var i = 0; i < changed.OldItems!.Count; i++)
                {
                    _toolHistoryMirror.RemoveAt(changed.OldStartingIndex);
                }

                break;
            case NotifyCollectionChangedAction.Replace:
            case NotifyCollectionChangedAction.Move:
                // TODO:
                break;
            case NotifyCollectionChangedAction.Reset:
                var comp = _entityManager.GetComponent<MapEditorClientMapDataComponent>(mapData);
                _toolHistoryMirror.Clear();
                _toolHistoryMirror.AddRange(comp.ToolEntityHistory);
                break;
            default:
                throw new ArgumentOutOfRangeException();
        }
    }

    private Control CreateHistoryEntry(EntityUid arg)
    {
        var control = new ContainerButton
        {
            MouseFilter = MouseFilterMode.Stop,
            StyleClasses = { BaseEditorStylesheet.StyleClassToolButton },
            Children =
            {
                new Control
                {
                    StyleClasses = { MapEditorStyleClasses.ToolPreview },
                    Children = { ActiveToolPanel.GetPreviewControl(arg, _entityManager, _resourceCache) }
                }
            }
        };
        control.TooltipSupplier = _ =>
        {
            var comp = _entityManager.GetComponent<MapEditorToolDataComponent>(arg);
            var tt = new Tooltip();
            tt.SetMessage(comp.ToolName);
            return tt;
        };
        control.OnPressed += _ => SelectFromHistory(arg);
        return control;
    }

    private void SelectFromHistory(EntityUid tool)
    {
        _mapEditor.SwitchToHistoryTool(_mapData, tool);
    }
}
