using System;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;

namespace Robust.Client.Editor.Interface;

/// <summary>
/// Represents an editor panel that can be docked around by dragging the tab header.
/// </summary>
[Virtual]
[GenerateTypedNameReferences]
public partial class EditorPanel : Control
{
    private bool _canClose = true;
    private string _title = "";

    public EditorTabPanel? CurrentParent { get; internal set; }

    public EditorPanel()
    {
        RobustXamlLoader.Load(this);
    }

    public string Title
    {
        get => _title;
        set
        {
            _title = value;
            TabInfoChanged?.Invoke();
        }
    }

    public bool CanUndock { get; set; } = true;

    public bool CanClose
    {
        get => _canClose;
        set
        {
            _canClose = value;
            TabInfoChanged?.Invoke();
        }
    }

    internal event Action? TabInfoChanged;

    [MustCallBase(true)]
    [Access(typeof(EditorTabPanel))]
    protected internal virtual void TabFocused()
    {
    }

    [MustCallBase(true)]
    [Access(typeof(EditorTab), typeof(EditorDocker))]
    protected internal virtual void TabClosed()
    {
    }
}
